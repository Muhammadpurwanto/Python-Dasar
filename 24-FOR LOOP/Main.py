# perulangan loop

angka = [0,1,2,3,4]
print(angka)

# dengan list
for i in angka:
    print(f"i sekarang {i}")
print("selesai")

# dengan range
for i in range(2,5):
    print(f"i sekarang {i}")
print("selesai")

# menggunakan string
data_str = "saya adalah ultramen"
for huruf in data_str:
    print(huruf)
