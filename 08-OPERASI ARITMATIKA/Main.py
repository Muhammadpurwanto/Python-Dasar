# operasi aritmatika

a = int(input("masukan nilai a: "))
b = int(input("masukan nilai b: "))

#operasi tambah
hasil = a + b
print(a,'+',b,'=', hasil)

#operasi kurang
hasil = a - b
print(a,'-',b,'=', hasil)

#operasi kali
hasil = a * b
print(a,'*',b,'=', hasil)

#operasi bagi
hasil = a / b
print(a,':',b,'=', hasil)

#operasi eksponen
hasil = a ** b
print(a,'**',b,'=', hasil)

#operasi modulus
hasil = a % b
print(a,'%',b,'=', hasil)

#operasi floor division
hasil = a // b
print(a,'//',b,'=', hasil)