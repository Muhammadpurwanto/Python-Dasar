# progam kenversi celcius ke satuan lain

print("\nPROGAM KENVERSI KE SATUAN LAIN")

celcius = float(input("masukan suhu dalam celcius: "))
print("suhu adalah ",celcius, "C")

# reamur 4/5C
reamur = (4/5)*celcius
print("suhu dalam reamur adalah ",reamur)

# fahrenheit ((9/5)*celcius)+32
fahrenheit = ((9/5)*celcius)+32
print("suhu dalam fahrenheit adalah ",fahrenheit)

# kelvin celcius+273
kelvin = celcius+273
print("suhu dalam kelvin adalah ",kelvin)
