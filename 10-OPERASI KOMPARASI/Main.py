# operasi komparasi
# setiap hasil operasi komparasi adalah boolean

a = 2
b = 4

# lebih besar dari >
hasil = a>b
print(hasil)

# kurang dari <
hasil = a<b
print(hasil)

# lebih besar atau sama dengan dari >=
hasil = a>=b
print(hasil)

# kurang atau sama dengan dari <=
hasil = a<=b
print(hasil)
# sama dengan dari ==
hasil = a==b
print(hasil)

# tidak sama dengan dari !=
hasil = a!=b
print(hasil)

# apakah is
hasil = a is b
print(hasil)

# tidak apakah is not
hasil = a is not b
print(hasil)
