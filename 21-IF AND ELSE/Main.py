# If dan Else Statement

nama = input("siapa nama anda? ")

# if inline
if nama=="ucup" : print("iya itu saya")
print(f"selesai {nama}")

# if indentation
if nama=="ucup" :
     print("iya itu saya")
     print("uhu")
print(f"selesai {nama}")

# else statement
if nama=="otong" : 
    print(f"hai {nama}")
else:
    print("salah")
print("akhir")
