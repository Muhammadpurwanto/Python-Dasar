# List -> array, mengakses dengan index

data_list = ['ucup', 'otong', 'dudung']
print(data_list[2])

# dictionary (dict) -> associative array
# identifier -> key

data_dict = {
    'key' : 'value',
    'cp' : 'ucup',
    'tg' : 'otong',
    'dg' : 'dudung',
    'nmbr' : 100,
    'list' : data_list
}
print(data_dict)
print(data_dict['tg'])
print(data_dict['nmbr'])
print(data_dict['list'])